const getDate = (date: Dayjs, weeks: number, weekday: number, hour: number) => (
  date
    .add(weeks, 'w')
    .weekday(weekday)
    .hour(hour)
    .minute(0)
    .second(0)
    .millisecond(0)
    .toISOString()
);

